import random,time

def dead_state(width,height): ## creates a grid of dead cells
    list = [[0 for i in range(width)]for i in range(height)]
    return list

def random_state(width,height):  ## creates a grid of randomly dead/alive cells
    state = dead_state(width,height)
    for i in range(height):
        for k in range(width):
            n = random.random()
            if n>=0.1:                                #######    5% of chance for a cell to be alive
                state[i][k]= ' '
            else:
                state[i][k]= '◼︎'
    return state


def render(grid):     ##.  render the grid
    if any(0 in row for row in grid):
        for i in range(len(grid)):
            for k in range(len(grid[0])):
                if grid[i][k]==0:
                    grid[i][k]= ' '
                else:
                    grid[i][k]= '◼︎'
    for i in range(len(grid)):
        print(*grid[i])
    print('\n\n\n')

def bit_to_color(bitgrid):
    for i in range(len(bitgrid)):
            for k in range(len(bitgrid[0])):
                if bitgrid[i][k]==0:
                    bitgrid[i][k]= ' '
                else:
                    bitgrid[i][k]= '◼︎'
    return bitgrid


#Any live cell with 0 or 1 live neighbors becomes dead, because of underpopulation
#Any live cell with 2 or 3 live neighbors stays alive, because its neighborhood is just right
#Any live cell with more than 3 live neighbors becomes dead, because of overpopulation
#Any dead cell with exactly 3 live neighbors becomes alive, by reproduction


def future_state(grid,line,column):  ### find out the next state of a given cell
    
    counter = 0
    for i in range(line-1,line+2):
        for k in range(column-1,column+2):
            if i==line and k==column:
                continue
            if i<len(grid):
                if k<len(grid[0]):
                    if grid[i][k]=='◼︎':
                        counter+=1



    if counter <= 1:
        return ' '
    elif counter > 3:
        return ' '
    elif counter == 3:
        return '◼︎'
    elif counter == 2:
        if grid[line][column]=='◼︎':
            return '◼︎'
        else:
            return ' '
    


def next_step(grid):   ####  find out the next step for the whole grid and print it
    nextgrid = []
    for i in range(len(grid)):
        nextgrid.append([future_state(grid,i,k) for k in range(len(grid[0]))])
    render(nextgrid)
    return nextgrid
    
